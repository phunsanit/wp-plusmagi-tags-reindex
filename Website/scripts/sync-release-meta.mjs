import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptFile = fileURLToPath(import.meta.url);
const scriptDir = path.dirname(scriptFile);
const websiteRoot = path.resolve(scriptDir, '..');
const repoRoot = path.resolve(websiteRoot, '..');
const pluginMainPath = path.join(repoRoot, 'SVN', 'trunk', 'plusmagi-tags-reindex.php');
const readmePath = path.join(repoRoot, 'SVN', 'trunk', 'readme.txt');
const outputPath = path.join(websiteRoot, 'src', 'generated', 'release-meta.ts');

function readText(filePath) {
  return fs.readFileSync(filePath, 'utf8');
}

function parsePluginHeader(headerText) {
  const pluginName = headerText.match(/^ \* Plugin Name:\s*(.+)$/m)?.[1]?.trim() || 'PlusMagi Tags Reindex';
  const version = headerText.match(/^ \* Version:\s*(.+)$/m)?.[1]?.trim() || '0.0.0';
  return { pluginName, version };
}

function parseChangelog(readmeText) {
  const sectionMatch = readmeText.match(/==\s*Changelog\s*==([\s\S]*)/i);
  if (!sectionMatch) {
    return [];
  }

  const lines = sectionMatch[1].split(/\r?\n/);
  const entries = [];
  let current = null;

  for (const line of lines) {
    const versionLine = line.match(/^=\s*([^=]+?)\s*=$/);
    if (versionLine) {
      if (current) {
        entries.push(current);
      }
      current = { version: versionLine[1].trim(), items: [] };
      continue;
    }

    if (!current) {
      continue;
    }

    const bullet = line.match(/^\*\s+(.+)$/);
    if (bullet) {
      current.items.push(bullet[1].trim());
    }
  }

  if (current) {
    entries.push(current);
  }

  return entries;
}

function buildReleaseMeta() {
  const pluginHeader = readText(pluginMainPath);
  const readme = readText(readmePath);

  const { pluginName, version } = parsePluginHeader(pluginHeader);
  const changelogEntries = parseChangelog(readme);

  const matched = changelogEntries.find((entry) => entry.version === version);
  const fallback = changelogEntries[0] || { version, items: ['Release notes are not available in readme.txt yet.'] };
  const selected = matched || fallback;

  return {
    pluginName,
    version,
    changelogVersion: selected.version,
    changelogItems: selected.items.length > 0 ? selected.items : ['Release notes are not available in readme.txt yet.'],
    sources: {
      pluginHeader: 'SVN/trunk/plusmagi-tags-reindex.php',
      readme: 'SVN/trunk/readme.txt',
    },
    generatedAt: new Date().toISOString(),
  };
}

function writeOutput(meta) {
  const content = `/* This file is auto-generated by Website/scripts/sync-release-meta.mjs */\nexport const releaseMeta = ${JSON.stringify(meta, null, 2)} as const;\n`;
  fs.writeFileSync(outputPath, content, 'utf8');
}

const meta = buildReleaseMeta();
writeOutput(meta);
console.log(`Synced release metadata: ${meta.pluginName} v${meta.version} -> changelog ${meta.changelogVersion}`);
